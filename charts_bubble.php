        protected function setupCharts()
        {
            $sql = 'SELECT *, (total_medals_won/team_size) as medals_per_athlete 
            FROM (%source%) s
            ORDER BY total_medals_won desc';
            $chart = new Chart('bubble_chart', Chart::TYPE_BUBBLE, $this->dataset, $sql);
            $chart->setTitle('2018 Winter Olympics (correlation between team sizes, total medals won, medals per athlete and gold medals won)');
            $chart->setHeight(400);
            $chart->addDataColumn('country_code', '', 'string');
            $chart->addDataColumn('team_size', 'Team size', 'number');
            $chart->addDataColumn('total_medals_won', 'Total medals won', 'number');
            $chart->addDataColumn('medals_per_athlete', 'Medals per athlete', 'float');
            $chart->addDataColumn('gold_medals_won', 'Gold medals won', 'number');
            $this->addChart($chart, 0, ChartPosition::BEFORE_GRID, 12);
        }
    
