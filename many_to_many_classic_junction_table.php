                $this->GetConnection()->ExecSQL($sql);
                $genres = GetApplication()->GetPOSTValue('genres_edit');
                foreach ($genres as $genre) {
                    $sql = sprintf('INSERT INTO movie_genres VALUES(%d, %d);', $rowData['id'], $genre);
                    $this->GetConnection()->ExecSQL($sql);
                }
            }
