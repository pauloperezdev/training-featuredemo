        protected function DoBeforeCreate()
        {
            $this->SetTitle('Starred in "%title%"');
            $this->SetMenuLabel('Actors By Movie');
    
            $selectQuery = 'SELECT
              ma.movie_id,
              ma.`character`,
              a.*
            FROM actors a
            INNER JOIN movie_actors ma 
              ON ma.actor_id = a.id';
            $insertQuery = array('INSERT INTO movie_actors
            VALUES
            (:movie_id, :id, :character)');
            $updateQuery = array('UPDATE movie_actors
            SET actor_id=:id, `character`=:character
            WHERE actor_id=:OLD_id AND movie_id=:OLD_movie_id');
            $deleteQuery = array('DELETE FROM movie_actors
            WHERE actor_id=:id AND movie_id=:movie_id');
            $this->dataset = new QueryDataset(
              MySqlIConnectionFactory::getInstance(), 
              GetConnectionOptions(),
              $selectQuery, $insertQuery, $updateQuery, $deleteQuery, 'q_actors_by_movies');
            $this->dataset->addFields(
                array(
                    new IntegerField('movie_id', false, true),
                    new StringField('character'),
                    new IntegerField('id', false, true),
                    new StringField('name'),
                    new DateField('birthday'),
                    new DateField('deathday'),
                    new StringField('about'),
                    new StringField('photo')
                )
            );
            $this->dataset->AddLookupField('movie_id', 'movies', new IntegerField('id'), new StringField('title', false, false, false, false, 'movie_id_title', 'movie_id_title_movies'), 'movie_id_title_movies');
            $this->dataset->AddLookupField('id', 'actors', new IntegerField('id'), new StringField('name', false, false, false, false, 'id_name', 'id_name_actors'), 'id_name_actors');
        }
    
